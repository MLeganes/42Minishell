#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t			count;

	if (dest == 0 && src == 0)
		return (NULL);
	if (dest > src)
	{
		while (n > 0)
		{
			((char *)dest)[n - 1] = ((char *)src)[n - 1];
			n--;
		}
	}
	else
	{
		count = 0;
		while (count < n)
		{
			((char *)dest)[count] = ((char *)src)[count];
			count++;
		}
	}
	return (dest);
}
